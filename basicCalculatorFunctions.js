// ADDITION FUNCTION 

function addition(firstNum = 0, secondNum = 0, thirdNum = 0, fourthNum = 0, fifthNum = 0) { 
    return firstNum + secondNum + thirdNum + fourthNum + fifthNum;
}

console.log(addition(100, 500, 1000, 25)); //

// SUBTRACTION FUNCTION 

function subtraction(num1 = 0, num2 = 0, num3 = 0, num4 = 0, num5 = 0) { 
    return num1 - num2 - num3 - num4 - num5; 
}

console.log(subtraction(5000, 2000, 250, 40, 300)); 

// MULTIPLICATION FUNCTION 

function multiplication(number1, number2, number3) {
    return number1 * number2 * number3; 
}

console.log(multiplication(1500, 2, 12)); 


// DIVISION FUNCTION 

function division(numero1 = 0 , numero2 = 0) { 
    return numero1 / numero2; 
}

console.log(division(100, 5)); 
